# Explain the data types in Python

print('-'*50)
number = 10
print(f"This is a number: {number} with type {type(number)}")
print('-'*50)


float_value = 2.5
print(f"This is a float: {float_value} with type {type(float_value)}")
print('-'*50)


complex_value = 1+2j
print(f"This is a complex: {complex_value} with type {type(complex_value)}")
print('-'*50)

string = "Welcome to CS60013 : Programming and Data Structures"
print(f"This is a string: {string} with type {type(string)}")
print('-'*50)

