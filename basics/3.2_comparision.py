# Let's look at the basic comparison operators in Python

num1 = 10
num2 = 20

sep_str = "-"*20

print(f"\n-------------------Comparison operators-------------------\n")

# 1. == (equal to)
print(f"{num1} == {num2} is {num1 == num2}")
print(sep_str)

# 2. != (not equal to)
print(f"{num1} != {num2} is {num1 != num2}")
print(sep_str)

# 3. < (less than)
print(f"{num1} < {num2} is {num1 < num2}")
print(sep_str)

# 4. > (greater than)
print(f"{num1} > {num2} is {num1 > num2}")
print(sep_str)

# 5. <= (less than or equal to)
print(f"{num1} <= {num2} is {num1 <= num2}")
print(sep_str)

# 6. >= (greater than or equal to)
print(f"{num1} >= {num2} is {num1 >= num2}")
print(sep_str)
